package JavaBasic.Kieudulieu;

public class Kieudulieu1 {

    public static void main(String[] args) {
        //String: là kiểu dữ liệu chuối
        String decription = "Đây là mô tả về Huy. Huy là một tester với 2 năm kinh nghiệm thực chiến, kiến thức cơ bản vừa đủ dùng cho dự án nhỏ, và đơn giản";
        System.out.println(decription);

        //byte: Kiểu dữ liệu số nguyên từ -127 -- 127
        byte number = 123;
        //char: lưu k tự 'a', 'b',...

        //boolean: có 2 giá trị là true hoặc fail
        boolean checkvalue = true;

        //short: lưu được 2 byte từ -32768 đến 32768
        short number2 = 3325;

        //int: Cũng là số nguyên nhưng khich cỡ là 4 byte (thông dụng - thường xuyên xử dụng)
        int number3 = 2100000000;

        //long: cũng số nguyên nhưng kich thước là 8 byte (phải để chữ L phía cuối)
        long number4 = 3000000000000000000L;

        //float: kiểu dữ liệu số thực (phải để chữ F phía cuối)
        float number5 = 3.9F;

        //double: kiểu số thực nhưng c kích thước 8 byte (phải để chữ D phía cuối)
        double number6 = 33243434343434.3434343434233;

        //Array: mãng để lưu các kiểu dữ liệu cùng kiểu
        //class: kiểu dữ liệu cho người dùng tự định nghĩa, đặt tên
    }
}
